<h1>Paris</h1>
    <p>Paris is the capital city of France. It is the most populous city in France, with a metropolitan area of over 12 million inhabitants.</p>
    <p>Standing on the River Seine, Paris has been a major settlement for two millennia, its history going back to its founding by the Romans, who named it Lutetia.</p>

