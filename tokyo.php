<h1>Tokyo</h1>
    <p>Tokyo is the capital city of Japan. It is the most populous city in Japan, with a metropolitan area of over 37 million inhabitants.</p>
    <p>Standing on the Sumida River, Tokyo has been a major settlement for centuries, evolving from Edo during the Edo period.</p>

